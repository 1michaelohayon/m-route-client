import { GoogleMap, useLoadScript, MarkerF } from "@react-google-maps/api"

const MAPZOOM = 11

interface MapArgs {
    mark: google.maps.LatLngLiteral
    dest: google.maps.LatLngLiteral
    center: google.maps.LatLngLiteral
}


const Map = ({ mark, dest, center }: MapArgs) => {
    const { isLoaded } = useLoadScript({
        language: 'he',
        googleMapsApiKey: process.env.REACT_APP_GOOGLE_API_KEY!
    })

    if (!isLoaded) {
        return <div>Loading...</div>
    }

    const mapOptions = {
        styles: retroStyle,
    };

    return <GoogleMap mapContainerStyle={mapStyle}
        zoom={MAPZOOM}
        center={center}
        options={mapOptions}
        mapContainerClassName="map"


    >
        <MarkerF position={mark} />
        <MarkerF position={dest} icon={customMarker} />
    </GoogleMap>
}

export default Map


